/**
 * add - add two numbers
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: On success 1
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subs two numbers
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: On success 1
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - return the remainder of a division
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: On success 1
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiplies two numbers
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: On success 1
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two numbers
 * @a: first number to be added
 * @b: second number to be added
 *
 * Return: On success 1
 */
int div(int a, int b)
{
	return (a / b);
}
